package com.dzakiganteng.basickotlin.basic

fun main() {
    //Literal Constant
    var decimalLiteral = 900
    var heksaDesimalLiteral = 0x0f
    var binaryLiteral = 0b0001011
    println(decimalLiteral)
    println(heksaDesimalLiteral)
    println(binaryLiteral)

    //Underscore
    var no : Int = 22345678
    var float : Float = no.toFloat()
    println(float)

    //Tipe data Character
    var c1 : Char = 'd'
    var c2 : Char = 'z'
    var c3 : Char = 'a'
    var c4 : Char = 'k'
    var c5 : Char = 'i'
    print(c1)
    print(c2)
    print(c3)
    print(c4)
    print(c5)

    //Special Character
    println("dzaki")
    println("\tdzaki")
    println("\bdzaki")
    println("\ndzaki")
    println("\rdzaki")
    println("\'dzaki")
    println("\"dzaki")
    println("\\dzaki")
    println("\$dzaki")
}
