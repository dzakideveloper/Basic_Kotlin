package com.dzakiganteng.basickotlin.operator

fun main() {
    val tambah : Int = 12 + 3
    println(tambah)
    val kurang : Int = 12 - 3
    println(kurang)
    val kali : Int = 12 * 3
    println(kali)
    val bagi : Int = 12 / 3
    println(bagi)
    val sisaBagi : Int = 12 % 3
    println(sisaBagi)

    //pangkat
    val pangkat : Double = Math.pow(2.0,3.0)
    println(pangkat)
}